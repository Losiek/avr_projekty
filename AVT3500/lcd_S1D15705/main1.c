void LCD_init(void)
{
  unsigned char init[13]={0xab,0xa3,0xa6,0xc8,0x40,0x2b,0x26,0x81,0x13,0xaf,0xb0,0x10,0x03};  //negatyw
  //unsigned char init[13]={0xab,0xa3,0xa6,0xc8,0x40,0x2b,0x26,0x81,0x13,0xaf,0xb0,0x10,0x03};  //pozytyw
  unsigned char i, j;
  LCD_INIT_GPIO();
  LCD_DATA_WRITE();
//  RESET=0;
  LCD_RST_CLR();
//  RESET=1;
  LCD_RST_SET();
  for(j=0xb0;j<0xb9;j++)
  {
    command_write(j);
    command_write(0x10);
    command_write(0x03);
    for(i=0;i<162;i++)
      if(j!=0xb8)
        data_write(0x00);
      else
//       data_write(0xff); //pozytyw
        data_write(0x00); //negatyw
  }
  for(i=0;i<13;i++)
    command_write(init[i]);
}

void LCD_pixel(unsigned char x,unsigned char y,unsigned char mode)
{
  unsigned char x_h, x_l;
  unsigned char tmp;
  unsigned char page;

  x+=3;
  x_h=(x>>4);
  x_l=x&0x0f;
  page=y/8;
  tmp=y-page*8;
  y=0x01<<tmp;
  if(mode==0)
    y=~y;
  command_write(0x40);
  command_write(0xb0|page);
  command_write(x_h|0x10);
  command_write(x_l);
  tmp=data_read();
  if(mode==0)
    y&=tmp;
  else
    y|=tmp;
  command_write(x_h|0x10);
  command_write(x_l);
  data_write(y);
}

