void LCD_rectangle(unsigned char x0,unsigned char y0,unsigned char x1,unsigned char y1,unsigned char mode,unsigned char fill)
{
  unsigned char i, j;

  for(i=x0;i<=x1;i++)
  {
    LCD_pixel(i,y0,mode);
    LCD_pixel(i,y1,mode);
    if(fill!=0)
      for(j=y0+1;j<y1;j++)
        LCD_pixel(i,j,mode);
  }
  for(i=y0;i<=y1;i++)
  {
    LCD_pixel(x0,i,mode);
    LCD_pixel(x1,i,mode);
  }
}

