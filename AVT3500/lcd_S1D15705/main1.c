void LCD_string(unsigned char *s,unsigned char x,unsigned char y,unsigned char type,unsigned char mode)
{
  unsigned char cx, sx;

  cx=x;
  sx=(((unsigned char)type>>4)&0x0f)+1;
  while(*s)
  {
    LCD_char(*s,cx,y,type,mode);
    cx+=sx+1;
    s++;
  }
}

